
var dia_dela_semana = "martes";

switch (dia_dela_semana) {
    case "lunes":
        console.log("hoy es lunes");
        break;

    case "martes":
        console.log("hoy es martes");
        break;

    case "miercoles":
        console.log("hoy es miercoles");
        break;

    default:
        console.log("hoy es otro dia");
        break;
}